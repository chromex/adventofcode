map = aoc.DataMatrix(input)

def ChkNeigh(x, y, lbl):
    global map
    if not map.IndexInBounds(x, y) or map[x, y] != lbl:
        return False
    return True

def Match(run, perim):
    for r in run:
        if r[2] == perim[2]:
            dx = abs(r[0] - perim[0])
            dy = abs(r[1] - perim[1])
            if (dx == 1 and dy == 0) or (dx == 0 and dy == 1):
                return True
    return False
    

def Scan(x, y):
    global map

    area = 0
    perim = []
    label = map[x, y]
    map.Mark(x, y)
    candidates = [(x, y)]
    while len(candidates) > 0:
        nx, ny = candidates.pop()

        area += 1
        if ChkNeigh(nx - 1, ny, label):
            if not map.IsMarked(nx - 1, ny):
                map.Mark(nx - 1, ny)
                candidates.append((nx - 1, ny))
        else:
            perim.append([nx, ny, aoc.Direction.LEFT, None])

        if ChkNeigh(nx + 1, ny, label):
            if not map.IsMarked(nx + 1, ny):
                map.Mark(nx + 1, ny)
                candidates.append((nx + 1, ny))
        else:
            perim.append([nx, ny, aoc.Direction.RIGHT, None])

        if ChkNeigh(nx, ny - 1, label):
            if not map.IsMarked(nx, ny - 1):
                map.Mark(nx, ny - 1)
                candidates.append((nx, ny - 1))
        else:
            perim.append([nx, ny, aoc.Direction.UP, None])

        if ChkNeigh(nx, ny + 1, label):
            if not map.IsMarked(nx, ny + 1):
                map.Mark(nx, ny + 1)
                candidates.append((nx, ny + 1))
        else:
            perim.append([nx, ny, aoc.Direction.DOWN, None])

    pcount = 0
    while len(perim) > 0:
        cur = perim.pop()
        run = [cur]
        found = True
        while found:
            found = False
            for i in range(len(perim)):
                c = perim[i]
                if Match(run, c):
                    run.append(c)
                    found = True
                    break
            if found:
                perim.remove(run[-1])
        pcount += 1

    return (area, pcount)

sum = 0
def Visit(x, y):
    global map, sum
    if not map.IsMarked(x, y):
        a, p = Scan(x, y)
        sum += a * p

map.Visit(Visit)

print(sum)